int	ft_isprint(int c)
{
	if ('A' <= c && c <= 'Z')
		return (1);
	if ('a' <= c && c <= 'z')
		return (2);
	if ('0' <= c && c <= '9')
		return (4);
	if (c == 32)
		return (64);
	if (32 < c && c <= 126)
		return (16);
	return (0);
}
